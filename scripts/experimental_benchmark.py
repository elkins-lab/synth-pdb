#!/usr/bin/env python3
"""
Physical Ground Truth Validation Suite.

This script benchmarks synth-pdb against peer-reviewed experimental data
for Human Ubiquitin (PDB: 1D3Z, BMRB: 6457).

SCIENTIFIC OBJECTIVE:
To demonstrate that the synthetic data generated by synth-pdb is
physically defensible by showing high correlation with real-world
high-resolution NMR measurements.
"""

import os
import sys
import argparse
import numpy as np
import matplotlib.pyplot as plt
from scipy.stats import pearsonr
import biotite.structure.io.pdb as pdb_io

# Add parent directory to path to ensure we can import synth_pdb
sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), "..")))

from synth_pdb.bmrb_api import BMRBAPI
from synth_pdb.chemical_shifts import predict_chemical_shifts
from synth_pdb.saxs import calculate_radius_of_gyration


def calculate_metrics(experimental, synthetic):
    """Calculate correlation and error metrics."""
    atoms = sorted(set(experimental.keys()) & set(synthetic.keys()))
    if not atoms:
        return None

    exp_vals = np.array([experimental[a] for a in atoms])
    syn_vals = np.array([synthetic[a] for a in atoms])

    r, _ = pearsonr(exp_vals, syn_vals)
    rmsd = np.sqrt(np.mean((exp_vals - syn_vals) ** 2))
    mae = np.mean(np.abs(exp_vals - syn_vals))

    return {
        "atoms": atoms,
        "exp": exp_vals,
        "syn": syn_vals,
        "pearson_r": r,
        "rmsd": rmsd,
        "mae": mae,
    }


def run_benchmark(pdb_id="1D3Z", bmrb_id="6457", output_dir="artifacts/benchmarks"):
    """Run the benchmark comparison."""
    os.makedirs(output_dir, exist_ok=True)

    print(f"[TEST] Benchmarking synth-pdb vs Experimental Data ({pdb_id}/{bmrb_id})...")

    # 1. Load/Download PDB
    pdb_path = f"examples/{pdb_id}.pdb"
    if not os.path.exists(pdb_path):
        print(f"   [DOWNLOAD] Downloading PDB {pdb_id}...")
        BMRBAPI.download_pdb(pdb_id, pdb_path)

    # 2. Fetch Experimental Shifts
    print(f"   [FETCH] Fetching BMRB {bmrb_id} experimental shifts...")
    exp_shifts_full = BMRBAPI.fetch_chemical_shifts(bmrb_id)
    if not exp_shifts_full:
        print("   [ERROR] Could not fetch experimental shifts.")
        return

    # 3. Generate Synthetic Shifts
    print("   [GEN] Generating synthetic shifts from structure...")
    # predict_chemical_shifts requires a biotite AtomArray
    pdb_file = pdb_io.PDBFile.read(pdb_path)
    structure = pdb_file.get_structure(model=1)

    # predict_chemical_shifts returns {chain: {res_id: {atom: val}}}
    syn_shifts_full = predict_chemical_shifts(structure)
    # Assume Chain A for 1D3Z
    syn_shifts = syn_shifts_full.get("A", {})
    if not syn_shifts:
        # Fallback if first key is different
        syn_shifts = list(syn_shifts_full.values())[0]

    # 4. Process and Align Data
    # We'll compare standard backbone atoms: HN, N, CA, CB, CO, HA
    target_atoms = ["HN", "N", "CA", "CB", "C", "HA"]
    results = {}

    print("   [STATS] Analyzing correlations...")

    for atom in target_atoms:
        exp_flat = {}
        syn_flat = {}

        # Standardize naming: BMRB uses 'C', some parsers use 'CO'
        query_atom = "C" if atom == "C" else atom

        for res_id in exp_shifts_full:
            if res_id in syn_shifts:
                if query_atom in exp_shifts_full[res_id] and atom in syn_shifts[res_id]:
                    exp_flat[res_id] = exp_shifts_full[res_id][query_atom]
                    syn_flat[res_id] = syn_shifts[res_id][atom]

        metrics = calculate_metrics(exp_flat, syn_flat)
        if metrics:
            results[atom] = metrics
            print(
                f"      - {atom:<3}: R = {metrics['pearson_r']:.3f}, RMSD = {metrics['rmsd']:.3f} ppm"
            )

    # 4b. Global Biophysics
    rg = calculate_radius_of_gyration(structure)
    print("\n   [BIOPHYSICS] Global Parameters:")
    print(f"      - Radius of Gyration (Rg): {rg:.2f} Angstroms")
    # Reference value for Ubiquitin Rg is ~11.5-12.0 A (depending on solvation)
    rg_ref = 11.55
    rg_error = abs(rg - rg_ref) / rg_ref * 100
    print(f"      - Ref (1D3Z): ~{rg_ref} A (Error: {rg_error:.1f}%)")

    # 5. Generate Visualizations
    if results:
        fig, axes = plt.subplots(2, 3, figsize=(15, 10))
        axes = axes.flatten()

        for i, atom in enumerate(target_atoms):
            if atom in results:
                m = results[atom]
                ax = axes[i]
                ax.scatter(m["exp"], m["syn"], alpha=0.6, edgecolors="white")

                # Diagonal line
                lims = [
                    np.min([ax.get_xlim(), ax.get_ylim()]),
                    np.max([ax.get_xlim(), ax.get_ylim()]),
                ]
                ax.plot(lims, lims, "k--", alpha=0.5, zorder=0)

                ax.set_title(f"{atom} (R={m['pearson_r']:.3f})")
                ax.set_xlabel("Experimental (ppm)")
                ax.set_ylabel("Synthetic (ppm)")

        plt.suptitle(f"Physical Ground Truth Benchmark: synth-pdb vs BMRB {bmrb_id}", fontsize=16)
        plt.tight_layout(rect=[0, 0.03, 1, 0.95])

        plot_path = os.path.join(output_dir, f"benchmark_{pdb_id}_{bmrb_id}.png")
        plt.savefig(plot_path, dpi=300)
        print(f"\n   [PLOT] Report saved to {plot_path}")

    # 6. Scientific Defensibility Verdict
    avg_r = np.mean([r["pearson_r"] for r in results.values()])
    print("\nFINAL VERDICT:")
    print(f"   Mean Pearson Correlation: {avg_r:.4f}")
    if avg_r > 0.90:
        print("   Status: PASSED (SCIENTIFICALLY DEFENSIBLE)")
        print("   Conclusion: The synth-pdb physics engine accurately reproduces")
        print("               experimental observations for high-resolution structures.")
    else:
        print("   Status: CAUTION")
        print("   Conclusion: Significant deviations detected. Check coordinate accuracy.")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="synth-pdb Physical Ground Truth Benchmark")
    parser.add_argument("--pdb", default="1D3Z", help="PDB ID to benchmark")
    parser.add_argument("--bmrb", default="6457", help="BMRB ID to benchmark")
    args = parser.parse_args()

    run_benchmark(args.pdb, args.bmrb)
